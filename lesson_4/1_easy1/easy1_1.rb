# Which of the following are objects in Ruby? If they are objects, 
# how can you find out what class they belong to?

# 1. true
# 2. "hello"
# 3. [1, 2, 3, "happy days"]
# 4. 142

# All of the above are objects. And you can figure out which class
# they belong to by calling the .class method on them:

p true.class
p "hello".class
p [1, 2, 3, "happy days"].class
p 142.class