# What are the benefits of using Object Oriented Programming in Ruby? 
# Think of as many as you can.

# Eliminates redundant code (through inheritance).
# Limits scope of code to specific classes.
# Allows instance and class methods to be public, protected, or private
#   to avoid unwanted usage outside of the class.
# Allows augmented functionality through Modules (which also cuts
#   down on redundant code.)
# Forces you to consider larger framework and heirarchy when designing
#   which can utlimately result in easier to understand code.